<p class="text">
पूर्व माध्यमिक विद्यालय गोठा विकास क्षेत्र वजीरगंज जनपद बदायूं प्रधानाध्यापक श्री नेत्रपाल सिंह चौहान, डॉ मनोज कुमार वार्ष्णेय स.अ., श्रीमती सुधा स.अ.| विद्यालय में मुख्य भूमिका डॉ मनोज कुमार वार्ष्णेय द्वारा निभाई जाती है  | विद्यालय में जनरेटर, इनवर्टर, समरसेबल, कंप्यूटर लैब, स्मार्ट क्लास हेतु प्रोजेक्टर, रेन वाटर हार्वेस्टिंग, सी.सी.टी.वी कैमरे बायोमेट्रिक उपस्थिति आदि सुविधाएं समुदाय के सहयोग से विद्यालय परिवार ने अपनी मेहनत के फल स्वरुप अर्जित की हैं विद्यालय में बाउंड्री वाल नहीं है फिर भी 200 से अधिक पौधे व अनेको प्रकार के फूल प्रत्येक मौसम में विद्यालय में खिलते हैं |</span> 
</p>
<p class="text">विद्यालय के छात्र शिवम साहू का प्रोजेक्ट गत वर्ष 2016 में राष्ट्रीय स्तर पर चुना गया जिसके लिए उसे मानव संसाधन विकास मंत्री श्री मान प्रकाश जावड़ेकर एवं पूर्व कृषि मंत्री शरद पवार ने बारामती महाराष्ट्र में सम्मानित किया इस वर्ष भी विद्यालय के छात्रों ने राज्य स्तर पर स्थान अर्जित किया वर्तमान सत्र में जल संरक्षण हेतु बच्चों द्वारा बनाया गया प्रोजेक्ट प्रदेश स्तर से राष्ट्रीय स्तर पर प्रतिभाग करने हेतु चयनित हो चुका है </p>
<p class="text">जनपद बदायूं के जिलाधिकारी श्री दिनेश कुमार सिंह द्वारा आयोजित जनपद स्तरीय दक्षता परीक्षा में विद्यालय के तीन छात्र छात्राओं ने अपना स्थान अर्जित किया है तथा पुरस्कार स्वरूप 3 साइकिल वार्ड नकद धनराशि प्राप्त की विद्यालय जनपद का एकमात्र सीसीटीवी कैमरे युक्त विद्यालय है | ग्राम पंचायत के सहयोग से विद्यालय में दिव्यांग शौचालय का निर्माण हो चुका है</p>
<p class="text">माननीय प्रधानमंत्री जी की महत्वाकांक्षी योजना व स्वच्छ भारत अभियान के अंतर्गत स्वच्छ विद्यालय पुरस्कार में इस वर्ष विद्यालय ने 5 स्टार रैंक प्राप्त की है</p>
 
<p class="text"> <span style="font-size:18px; font-weight:600;color: #d47a07;">स्कूल में उपलब्द्ध सुविधाएं-</span> <br>जब शिक्षक मनोज कुमार वार्ष्णेय ने विद्यालय में जॉइन किया तब विद्यालय का नामांकन 61 था तथा औसत उपस्थिति 40 से 50% रहती थी वर्तमान सत्र में नामांकन 138 है उपस्थिति 90 से 100 % के मध्य रहती है तथा 26 अगस्त से 5 दिसंबर के मध्य लगातार 72 कार्य दिवसों में  विद्यालय में शत प्रतिशत उपस्थिति रही जो बायोमेट्रिक मशीन द्वारा दर्ज की गई तथा ब्लॉक एवं जनपद स्तर के अधिकारियों के साथ विभिन्न अन्य जनपदों के अधिकारियों ने भी इस शत प्रतिशत उपस्थिति का भौतिक सत्यापन किया उपस्थिति बढ़ाने के लिए विद्यालय द्वारा कुछ नवाचार क्रियान्वित किये गये</p>

